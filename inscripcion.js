class Inscripcion{
    name;
    firstLastName;
    secondLastName;
    gender;
    grade;
    type;

    constructor(name,firstLastName,secondLastName,gender,grade, type){
        this.name = name;
        this.firstLastName = firstLastName;
        this.secondLastName = secondLastName;
        this.gender = gender;
        this.grade = grade;
        this.type= type;
    }
}